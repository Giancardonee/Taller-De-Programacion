public class Fecha {
    private String ciudad;
    private String dia; 
    
    public Fecha (){
        
    }
    
    public Fecha (String dia, String ciudad){
        this.dia = dia;
        this.ciudad = ciudad;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public String getDia() {
        return dia;
    }

    public void String(String dia) {
        this.dia = dia;
    }
    
    
    
}
